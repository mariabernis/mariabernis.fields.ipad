#import "UIColor+Fields.h"

@implementation UIColor (Fields)

+ (UIColor *) fieldsGreen {
    return [UIColor colorWithRed:0.454 green:0.756 blue:0.368 alpha:1];
}

+ (UIColor *) fieldsBlue {
    return [UIColor colorWithHue:0.58 saturation:0.492 brightness:0.278 alpha:1];
}

+ (UIColor *) fieldsLightGreen {
    return [UIColor colorWithRed:0.885 green:0.915 blue:0.81 alpha:1];
}

+ (UIColor *) fieldsLightOcre {
    return [UIColor colorWithRed:0.933 green:0.933 blue:0.902 alpha:1];
}

@end
